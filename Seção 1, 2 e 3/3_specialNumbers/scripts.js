// Representa o valor infinito positivo
console.log(Infinity);

// Representa o valor infinito negativo
console.log(-Infinity);

// Not-a-Number: resultado inválido de uma operação numérica
console.log(NaN);

// Mostra o tipo primitivo de Infinity
console.log(typeof Infinity);

// Mostra o tipo primitivo de -Infinity
console.log(typeof -Infinity);

// Mostra o tipo primitivo de NaN
console.log(typeof NaN);
