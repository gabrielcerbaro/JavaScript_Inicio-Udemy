function stringLength (string) {
    
    if (string.length > 10) {
        console.log("Essa string é muito longa, digite uma abaixo de 10 caracteres");
    } else {
        console.log("Sua string esta dentro dos limites");
    }
}

stringLength("sakdsdsjdfalsfgslfgasl");
stringLength("gabriel");
stringLength("gabrielcer");
