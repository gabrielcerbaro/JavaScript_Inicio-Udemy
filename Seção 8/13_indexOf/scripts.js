// Este script demonstra os métodos indexOf() e lastIndexOf() para encontrar posições em arrays.
// indexOf() retorna o primeiro índice de um elemento, lastIndexOf() o último.
// Aqui, procuramos pelo número 2 em um array.
// Isso é útil para localizar elementos ou verificar existência.
// Como usar: Execute o script e veja os índices retornados.
// Princípio: Busca linear - eficiente para arrays pequenos.

let nums = [1,4,6,2,5,2,5,6];

console.log(nums.indexOf(2)); // 3 (primeira ocorrência de 2)

console.log(nums.lastIndexOf(2)); // 5 (última ocorrência de 2)