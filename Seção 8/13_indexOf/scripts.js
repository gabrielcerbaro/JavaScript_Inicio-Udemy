// Este script mostra como encontrar a posicao de um valor dentro de um array.
// indexOf() retorna a primeira posicao encontrada.
// lastIndexOf() retorna a ultima posicao encontrada.

let nums = [1,4,6,2,5,2,5,6];

// Retorna 3, pois o primeiro numero 2 esta no indice 3.
console.log(nums.indexOf(2));

// Retorna 5, pois o ultimo numero 2 esta no indice 5.
console.log(nums.lastIndexOf(2));
