// Este script mostra como criar arrays em JavaScript.
// Um array guarda varios valores dentro de uma unica variavel.
// Cada valor fica em uma posicao numerada, chamada indice.

// Array com numeros.
let arr = [1, 2, 3, 4, 5, 6, 7];

// Array com textos, tambem chamados de strings.
let str = ["Gabriel", "Gabriela", "Cerbaro"];

// Array com valores booleanos: true significa verdadeiro e false significa falso.
let bool = [true, false, true];

// Array misto, com tipos de dados diferentes no mesmo array.
let mist = ["Gabriel", 24, true];

// Exibe cada array no console do navegador.
console.log(arr);
console.log(str);
console.log(bool);
console.log(mist);
